n = input('Value: ')
print('float {}'.format(n))
print('bool {}'.format(n))
print('int {}'.format(n))
print('str {}'.format(n))
print('IS NUMERIC {}'.format(n.isnumeric()))
print('IS DECIMAL {}'.format(n.isdecimal()))
print('IS LOWER {}'.format(n.islower()))
